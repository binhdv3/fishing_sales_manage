package com.example.binhdv35.fishing_sales_manage.model;

public class OderItem {

    private String id, idOder, idProduct;
    private int  quantity, subtutol;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getIdOder() {
        return idOder;
    }

    public void setIdOder(String idOder) {
        this.idOder = idOder;
    }

    public String getIdProduct() {
        return idProduct;
    }

    public void setIdProduct(String idProduct) {
        this.idProduct = idProduct;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    public int getSubtutol() {
        return subtutol;
    }

    public void setSubtutol(int subtutol) {
        this.subtutol = subtutol;
    }
}
